import struct

width = 3
height = 3

# Каждый пиксель — это (R, G, B, A), всего 9 пикселей:
pixels = [
    (0x00, 0x00, 0x00, 0x00),
    (0x00, 0x00, 0x00, 0x00),
    (0x00, 0x00, 0x00, 0x00),

    (0x00, 0x00, 0x00, 0x00),
    (0x80, 0x80, 0x80, 0x00),
    (0x00, 0x00, 0x00, 0x00),

    (0x00, 0x00, 0x00, 0x00),
    (0x00, 0x00, 0x00, 0x00),
    (0x00, 0x00, 0x00, 0x00),
]

with open("in.data", "wb") as f:
    f.write(struct.pack("<II", width, height))  # 03000000 03000000
    for pixel in pixels:
        f.write(struct.pack("BBBB", *pixel))
