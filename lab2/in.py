import struct

width = 3
height = 3

pixels = [
    (0x01, 0x02, 0x03, 0x00),
    (0x04, 0x05, 0x06, 0x00),
    (0x07, 0x08, 0x09, 0x00),
    (0x09, 0x08, 0x07, 0x00),
    (0x06, 0x05, 0x04, 0x00),
    (0x03, 0x02, 0x01, 0x00),
    (0x00, 0x00, 0x00, 0x00),
    (0x14, 0x14, 0x14, 0x00),
    (0x00, 0x00, 0x00, 0x00),
]

with open("in.data", "wb") as f:
    f.write(struct.pack("<II", width, height))
    for pixel in pixels:
        f.write(struct.pack("BBBB", *pixel))
